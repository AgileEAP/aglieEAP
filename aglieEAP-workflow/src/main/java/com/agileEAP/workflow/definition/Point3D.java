package com.agileEAP.workflow.definition;

public final class Point3D
{
	private double x;
	public double getX()
	{
		return x;
	}
	public void setX(double value)
	{
		x = value;
	}

	private double y;
	public double getY()
	{
		return y;
	}
	public void setY(double value)
	{
		y = value;
	}

	private int z;
	public int getZ()
	{
		return z;
	}
	public void setZ(int value)
	{
		z = value;
	}
}